import os
import sys
import json
import random
import argparse

import numpy as np


"""
Improving dataset generation with ideas from "Budgeting Time"
- generation of dataset parameters:
    - end of day: t_max (minutes)
    - service area: x_max, y_max (meters)
    - truck count: 3
    - truck speed: 25 km/h (converted to meters per minute)
    - depot locations: d_0 and d_1
    - number of customers: c
    - expected number of requests: n
    - degree of dynamism: dod
        - number of early requests: n_0 = (1 - dod) * n
        - number of late requests: n_1 = n - n_0
    - expected truck loading and unloading times: t_load and t_unload
    - standard deviation of truck loading and unloading times: s_load and s_unload
- dataset generation:
    - c customers are generated with locations uniformly spread across [0, x_max] and [0, y_max]
    - n total requests are generated from a Poisson then split into n_0 and n_1 with dod
    - late requests are given arrival times generated by Gaussian
        - centered at t_max/3 and truncated to [0, t_max * 2/3]
    - truck loading and unloading times are generated by Gaussian
        - centered at t_load and t_unload and truncated to 2 sigma
"""

# global dataset parameters =================================
END_OF_DAY = 12 * 60

SERVICE_AREA_X = 20 * 1000
SERVICE_AREA_Y = 40 * 1000

TRUCK_COUNT = 3
TRUCK_SPEED = 25 * 1000 / 60

DEPOT_COUNT = 2
DEPOT_POS_LIST = (
    (10 * 1000, 10 * 1000),
    (10 * 1000, 30 * 1000))

CUSTOMER_COUNT = 30

EXP_REQUEST_COUNT = 15
DEGREE_OF_DYNAMISM = 0.3

EXP_LOADING_TIME = 90
EXP_UNLOADING_TIME = 120

STD_LOADING_TIME = 20
STD_UNLOADING_TIME = 20
# ===========================================================


def random_pos() -> tuple[float, float]:
    """
    Generate a random position uniformly
    """
    return (random.random() * SERVICE_AREA_X,
            random.random() * SERVICE_AREA_Y)


def random_request_num() -> tuple[int, int]:
    """
    Generate a random number of requests from a Poisson
    """
    n = np.random.poisson(EXP_REQUEST_COUNT)
    n_late = round(n * DEGREE_OF_DYNAMISM)
    n_early = n - n_late

    return n_early, n_late


def truncated_gaussian(mean: float, sigma: float) -> float:
    return min(mean + sigma*2, max(mean - sigma*2, random.gauss(mean, sigma)))


def random_order_time() -> float:
    mean = END_OF_DAY/3
    sigma = END_OF_DAY/6

    return truncated_gaussian(mean, sigma)


def random_loading_time() -> float:
    mean = float(EXP_LOADING_TIME)
    sigma = float(STD_LOADING_TIME)

    return truncated_gaussian(mean, sigma)


def random_unloading_time() -> float:
    mean = float(EXP_UNLOADING_TIME)
    sigma = float(STD_UNLOADING_TIME)

    return truncated_gaussian(mean, sigma)


if __name__ == '__main__':
    parser = argparse.ArgumentParser()
    parser.add_argument("path", help="Name of generated dataset", type=str)
    parser.add_argument("num", help="Number of tests to generate", type=int)
    parser.add_argument("-v", "--verbose", help="increase output verbosity", action="store_true")
    args = parser.parse_args()

    path = "./datasets/"
    path = os.path.join(path, args.path)

    if os.path.exists(path):
        print(f"{path} already exists\n"
              f"Please move or delete the already existing dataset")
        sys.exit(1)

    try:
        os.mkdir(path)
    except Exception as e:
        print(f"An error occurred: {e}")
        sys.exit(1)

    for i in range(args.num):
        result = {
            "end of day": END_OF_DAY,
            "service area": (SERVICE_AREA_X, SERVICE_AREA_Y),
            "truck count": TRUCK_COUNT,
            "truck speed": TRUCK_SPEED,
            "depots":
                DEPOT_POS_LIST,
            "customers":
                [random_pos() for _ in range(CUSTOMER_COUNT)],
            "expected request count": EXP_REQUEST_COUNT,
            "degree of dynamism": DEGREE_OF_DYNAMISM,
            "expected loading time": EXP_LOADING_TIME,
            "expected unloading time": EXP_UNLOADING_TIME,
            "std loading time": STD_LOADING_TIME,
            "std unloading time": STD_UNLOADING_TIME,
            "trucks":
                [{"start_depot": random.randint(0, DEPOT_COUNT - 1)} for _ in range(TRUCK_COUNT)],
        }

        n_early, n_late = random_request_num()
        requests = []
        for _ in range(n_early):
            request = {
                "src": random.randint(0, DEPOT_COUNT-1),
                "dst": random.randint(0, CUSTOMER_COUNT-1),
                "available_time": -1.0,
                "loading_duration": random_loading_time(),
                "unloading_duration": random_unloading_time(),
            }
            requests.append(request)
        for _ in range(n_late):
            request = {
                "src": random.randint(0, DEPOT_COUNT-1),
                "dst": random.randint(0, CUSTOMER_COUNT-1),
                "available_time": random_order_time(),
                "loading_duration": random_loading_time(),
                "unloading_duration": random_unloading_time(),
            }
            requests.append(request)
        result["requests"] = requests

        json_object = json.dumps(result, indent=4)
        if args.verbose:
            print(json_object)

        with open(os.path.join(path, f'test_{i}.json'), "w") as f:
            f.write(json_object)
